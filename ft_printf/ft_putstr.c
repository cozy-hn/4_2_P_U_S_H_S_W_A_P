#include "ft_printf.h"

int	ft_putstr(char *str, int *count)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (ft_putchar(str[i], count) == -1)
			return (-1);
		i++;
	}
	return (0);
}
